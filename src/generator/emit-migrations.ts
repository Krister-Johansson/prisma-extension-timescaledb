// Emit reset-safe migration files as a pure { relativePath -> content } map (no filesystem,
// no timestamps), so output is deterministic and trivially snapshot-testable.
//
// Two fixed-name migrations (the "managed migrations" strategy):
//   00000000000000_timescaledb_extension  -> sorts FIRST  (CREATE EXTENSION, constraint 1)
//   99999999999999_timescaledb_objects     -> sorts LAST   (after Prisma's CREATE TABLEs)
// Within the objects migration: all hypertable conversions first, then continuous aggregates
// (a cagg requires its source to already be a hypertable).
import type { TimescaleSchema } from "./dmmf.js";
import type { CaggConfig } from "../core/types.js";
import { createExtensionSql } from "../core/extension.js";
import { createHypertableSql } from "../core/hypertable.js";
import { createContinuousAggregateSql } from "../core/continuousAggregate.js";
import { createRetentionPolicySql } from "../core/retention.js";
import { createCompressionPolicySql } from "../core/compression.js";
import { createChunkSkippingSql } from "../core/chunkSkipping.js";

export const EXTENSION_MIGRATION = "00000000000000_timescaledb_extension";
export const OBJECTS_MIGRATION = "99999999999999_timescaledb_objects";

export type FileMap = Record<string, string>;

const GENERATED_BANNER = "-- AUTO-GENERATED by prisma-extension-timescaledb. Do not edit by hand.";

/**
 * Order continuous aggregates so a hierarchical cagg's source cagg is emitted (created) before it
 * — a topological sort over the `source`-is-another-cagg dependency. Independent caggs keep a
 * stable alphabetical order for deterministic output; a dependency cycle throws.
 */
function orderCaggs(caggs: readonly CaggConfig[]): CaggConfig[] {
  // Identity is (schema, name): under multiSchema two caggs can share a view name across schemas,
  // so keying by name alone could collide and resolve the wrong parent.
  const qualify = (schema: string | undefined, name: string): string => (schema ? `${schema}.${name}` : name);
  const byKey = new Map(caggs.map((c) => [qualify(c.schema, c.name), c]));
  const sorted: CaggConfig[] = [];
  const done = new Set<string>();
  const onPath = new Set<string>();
  const visit = (c: CaggConfig): void => {
    const key = qualify(c.schema, c.name);
    if (done.has(key)) return;
    if (onPath.has(key)) {
      throw new Error(`Continuous aggregate dependency cycle detected at "${c.name}".`);
    }
    onPath.add(key);
    const parent = byKey.get(qualify(c.sourceSchema, c.source)); // a source that is itself a cagg
    if (parent) visit(parent);
    onPath.delete(key);
    done.add(key);
    sorted.push(c);
  };
  for (const c of [...caggs].sort((a, b) => qualify(a.schema, a.name).localeCompare(qualify(b.schema, b.name)))) {
    visit(c);
  }
  return sorted;
}

/** Build the reset-safe migration file map for a parsed schema. Empty if no timescale objects. */
export function emitMigrations(schema: TimescaleSchema): FileMap {
  const hypertables = [...schema.hypertables].sort((a, b) => a.table.localeCompare(b.table));
  const caggs = orderCaggs(schema.continuousAggregates);

  if (hypertables.length === 0 && caggs.length === 0) return {};

  const files: FileMap = {};

  files[`${EXTENSION_MIGRATION}/migration.sql`] = `${GENERATED_BANNER}
-- TimescaleDB extension setup. Standalone & leading so it runs before any table or
-- hypertable DDL (CLAUDE.md constraint 1).
${createExtensionSql().up}
`;

  const parts: string[] = [];
  for (const h of hypertables) {
    parts.push(`-- Hypertable: ${h.table}\n${createHypertableSql(h).up}`);
    if (h.retention) {
      const sql = createRetentionPolicySql({
        table: h.table,
        ...(h.schema !== undefined ? { schema: h.schema } : {}),
        dropAfter: h.retention.dropAfter,
      }).up;
      parts.push(`-- Retention policy: ${h.table}\n${sql}`);
    }
    if (h.compression) {
      const c = h.compression;
      const sql = createCompressionPolicySql({
        table: h.table,
        ...(h.schema !== undefined ? { schema: h.schema } : {}),
        after: c.after,
        ...(c.segmentBy ? { segmentBy: c.segmentBy } : {}),
        ...(c.orderBy ? { orderBy: c.orderBy } : {}),
      }).up;
      parts.push(`-- Compression (columnstore) policy: ${h.table}\n${sql}`);
    }
    if (h.chunkSkipping && h.chunkSkipping.length > 0) {
      // After compression: chunk skipping records its range stats when chunks compress.
      const sql = createChunkSkippingSql({
        table: h.table,
        ...(h.schema !== undefined ? { schema: h.schema } : {}),
        columns: h.chunkSkipping,
      }).up;
      parts.push(`-- Chunk skipping: ${h.table}\n${sql}`);
    }
  }
  for (const c of caggs) {
    parts.push(`-- Continuous aggregate: ${c.name}\n${createContinuousAggregateSql(c).up}`);
  }

  files[`${OBJECTS_MIGRATION}/migration.sql`] = `${GENERATED_BANNER}
-- Hypertable conversions and continuous aggregates. Sorts last so the tables Prisma
-- created in its own migrations already exist. All DDL is idempotent (constraint 3).

${parts.join("\n\n")}
`;

  return files;
}
